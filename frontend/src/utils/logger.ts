export class Logger {
  info(...args: any[]) {
    // eslint-disable-next-line no-console
    console.info(...args);
  }
  error(...args: any[]) {
    // eslint-disable-next-line no-console
    console.error(...args);
  }
  warn(...args: any[]) {
    // eslint-disable-next-line no-console
    console.warn(...args);
  }
}


