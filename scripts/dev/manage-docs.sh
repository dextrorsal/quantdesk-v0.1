        echo -e "${RED}âŒ Empty file found: $file${NC}"
        echo -e "${GREEN}ğŸ’¡ Consider removing or fixing: $file${NC}"
    done
    
    # Fix files without headers
    echo -e "${YELLOW}ğŸ” Checking for files without headers...${NC}"
    find docs -name "*.md" -exec sh -c 'if [ ! -s "$1" ] || ! grep -q "^#" "$1"; then echo "âŒ No headers in: $1"; fi' _ {} \;
    
    # Fix very large files
    echo -e "${YELLOW}ğŸ” Checking for very large files...${NC}"
    find docs -name "*.md" -exec sh -c 'if [ $(wc -l < "$1") -gt 1000 ]; then echo "âš ï¸ Large file: $1 ($(wc -l < "$1") lines)"; fi' _ {} \;
    
    echo -e "${GREEN}âœ… Common issues check completed${NC}"
    echo ""
    read -p "Press Enter to continue..."
}

# Function to generate report
generate_report() {
    echo -e "${BLUE}ğŸ“ˆ Generating Documentation Report...${NC}"
    echo "===================================="
    
    local report_file="reports/docs/documentation-summary-$(date +%Y%m%d-%H%M%S).md"
