# Function to view structure
view_structure() {
    echo -e "${BLUE}ğŸ“‹ Documentation Structure${NC}"
    echo "=========================="
    echo ""
    
    if [ -d "docs" ]; then
        echo -e "${GREEN}ğŸ“ Current Documentation:${NC}"
